#!/usr/bin/env python3
"""
  concat - function that takes two strings
    and returns a concatenated string
  @str1: first string
  @str2: second string
  return: concat string
"""


def concat(str1: str, str2: str) -> str:
    """ concat function shold return a string """
    output: str = str(str1 + str2)
    return output
